# https://leetcode.com/problems/linked-list-cycle/discuss/44494/Except-ionally-fast-Python
class Solution:
    def hasCycle(self, head: Optional[ListNode]) -> bool:
        try:
            slow = head
            fast = head.next
            while slow is not fast:
                slow = slow.next
                fast = fast.next.next
            return True
        except:
            return False
